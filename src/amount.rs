pub type Amount = u64;

pub static COIN: Amount = 100_000_000;
pub static CENT: Amount = 100_000;
pub static MAX_MONEY: Amount = 300_000_000 * COIN;

pub fn money_range(value: Amount) -> bool {
    value <= MAX_MONEY
}
